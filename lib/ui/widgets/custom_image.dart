import 'package:flutter/material.dart';
import 'package:school_profile/index.dart';

class CustomImage extends Container {
  CustomImage(
    String path, {
    Key? key,
    text,
    bundle,
    frameBuilder,
    errorBuilder,
    semanticLabel,
    excludeFromSemantics = false,
    scale,
    width,
    height,
    color,
    colorBlendMode,
    fit,
    alignment = Alignment.center,
    repeat = ImageRepeat.noRepeat,
    centerSlice,
    matchTextDirection = false,
    gapLessPlayback = false,
    isAntiAlias = false,
    package,
    filterQuality = FilterQuality.low,
    cacheWidth,
    cacheHeight,
    EdgeInsetsGeometry? margin,
    EdgeInsetsGeometry? padding,
    String? title,
    double? verticalGap,
    double? fontSize,
    Color? titleColor,
    MainAxisAlignment? imageAndTitleAlignment,
    bool upperShadow = false,
  }) : super(
            key: key,
            margin: margin,
            padding: padding,
            child: Stack(
              children: [
                Column(
                  mainAxisAlignment: imageAndTitleAlignment ?? MainAxisAlignment.center,
                  children: [
                    Image.asset(
                      path,
                      bundle: bundle,
                      cacheHeight: cacheHeight,
                      cacheWidth: cacheWidth,
                      package: package,
                      scale: scale,
                      frameBuilder: frameBuilder,
                      errorBuilder: errorBuilder,
                      semanticLabel: semanticLabel,
                      excludeFromSemantics: excludeFromSemantics,
                      width: width,
                      height: height,
                      color: color,
                      colorBlendMode: colorBlendMode,
                      fit: fit,
                      alignment: alignment,
                      repeat: repeat,
                      centerSlice: centerSlice,
                      matchTextDirection: matchTextDirection,
                      gaplessPlayback: gapLessPlayback,
                      isAntiAlias: isAntiAlias,
                      filterQuality: filterQuality,
                    ),
                    if (title != null) SizedBox(height: verticalGap ?? BrandSizes.vMarginComment),
                    if (title != null)
                      CustomText(
                        text: title,
                        fontSize: fontSize ?? BrandSizes.h5,
                        color: titleColor ?? const Color(0xff666666),
                      )
                  ],
                ),
                if (upperShadow)
                  Container(
                    height: BrandSizes.screenTopShadowHeight,
                    decoration: const BoxDecoration(
                      gradient: LinearGradient(
                        colors: [Color(0xde000000), Color(0x00000000)],
                        stops: [0, 1],
                        begin: Alignment.topCenter,
                        end: Alignment.center,
                      ),
                    ),
                  )
              ],
            ));

  CustomImage.s1(String path,
      {Key? key,
      Color? color,
      BoxFit? fit,
      MainAxisAlignment? imageAndTitleAlignment,
      bool upperShadow = false,
      EdgeInsetsGeometry? margin,
      EdgeInsetsGeometry? padding,
      String? title,
      double? verticalGap,
      double? fontSize,
      Color? titleColor})
      : this(
          path,
          key: key,
          imageAndTitleAlignment: imageAndTitleAlignment,
          height: BrandSizes.s1,
          width: BrandSizes.s1,
          color: color,
          upperShadow: upperShadow,
          padding: padding,
          margin: margin,
          title: title,
          verticalGap: verticalGap,
          fontSize: fontSize,
          titleColor: titleColor,
        );

  CustomImage.s2(String path,
      {Key? key,
      Color? color,
      BoxFit? fit,
      MainAxisAlignment? imageAndTitleAlignment,
      bool upperShadow = false,
      EdgeInsetsGeometry? margin,
      EdgeInsetsGeometry? padding,
      String? title,
      double? verticalGap,
      double? fontSize,
      Color? titleColor})
      : this(
          path,
          key: key,
          imageAndTitleAlignment: imageAndTitleAlignment,
          height: BrandSizes.s2,
          width: BrandSizes.s2,
          color: color,
          upperShadow: upperShadow,
          padding: padding,
          margin: margin,
          title: title,
          verticalGap: verticalGap,
          fontSize: fontSize,
          titleColor: titleColor,
        );

  CustomImage.s3(String path,
      {Key? key,
      Color? color,
      BoxFit? fit,
      MainAxisAlignment? imageAndTitleAlignment,
      bool upperShadow = false,
      EdgeInsetsGeometry? margin,
      EdgeInsetsGeometry? padding,
      String? title,
      double? verticalGap,
      double? fontSize,
      Color? titleColor})
      : this(
          path,
          key: key,
          imageAndTitleAlignment: imageAndTitleAlignment,
          height: BrandSizes.s3,
          width: BrandSizes.s3,
          color: color,
          upperShadow: upperShadow,
          padding: padding,
          margin: margin,
          title: title,
          verticalGap: verticalGap,
          fontSize: fontSize,
          titleColor: titleColor,
        );

  CustomImage.s4(String path,
      {Key? key,
      Color? color,
      BoxFit? fit,
      MainAxisAlignment? imageAndTitleAlignment,
      bool upperShadow = false,
      EdgeInsetsGeometry? margin,
      EdgeInsetsGeometry? padding,
      String? title,
      double? verticalGap,
      double? fontSize,
      Color? titleColor})
      : this(
          path,
          key: key,
          imageAndTitleAlignment: imageAndTitleAlignment,
          height: BrandSizes.s4,
          width: BrandSizes.s4,
          color: color,
          upperShadow: upperShadow,
          padding: padding,
          margin: margin,
          title: title,
          verticalGap: verticalGap,
          fontSize: fontSize,
          titleColor: titleColor,
        );

  CustomImage.s5(String path,
      {Key? key,
      Color? color,
      BoxFit? fit,
      MainAxisAlignment? imageAndTitleAlignment,
      bool upperShadow = false,
      EdgeInsetsGeometry? margin,
      EdgeInsetsGeometry? padding,
      String? title,
      double? verticalGap,
      double? fontSize,
      Color? titleColor})
      : this(
          path,
          key: key,
          imageAndTitleAlignment: imageAndTitleAlignment,
          height: BrandSizes.s5,
          width: BrandSizes.s5,
          color: color,
          upperShadow: upperShadow,
          padding: padding,
          margin: margin,
          title: title,
          verticalGap: verticalGap,
          fontSize: fontSize,
          titleColor: titleColor,
        );

  CustomImage.s6(String path,
      {Key? key,
      Color? color,
      BoxFit? fit,
      MainAxisAlignment? imageAndTitleAlignment,
      bool upperShadow = false,
      EdgeInsetsGeometry? margin,
      EdgeInsetsGeometry? padding,
      String? title,
      double? verticalGap,
      double? fontSize,
      Color? titleColor})
      : this(
          path,
          key: key,
          imageAndTitleAlignment: imageAndTitleAlignment,
          height: BrandSizes.s6,
          width: BrandSizes.s6,
          color: color,
          upperShadow: upperShadow,
          padding: padding,
          margin: margin,
          title: title,
          verticalGap: verticalGap,
          fontSize: fontSize,
          titleColor: titleColor,
        );
}
